// Orden de las operaciones

// seria () * / - +

let resultado;

resultado = 20 + 30 * 2;

resultado = (100 + 200 + 300) * 0.2;
resultado = (100 + 200 + 300) * 1.19;
resultado = (600 + 600) * 1.05;

console.log(resultado);

// Incrementos

let puntaje = 10;

// incrementto en 1 ++
// post
puntaje++;
// pre incremento
++puntaje;

// incrementa en 10
puntaje += 10;

console.log(puntaje);

// decremento
puntaje--;
puntaje++;
console.log(puntaje);
